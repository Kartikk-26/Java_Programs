public class switchWork {
    public static void main(String[] args){
        String day ="Sunday";
        switch(day){
            case"Sunday":
            System.out.println("Today is Sunday");
            break;
            case"Monday":
            System.out.println("Today is Monday");
            break;
            case"Tuesday":
            System.out.println("Today is Tuesday");
            break;
            case"Wednesday":
            System.out.println("Today is Wednesday");
            break;
        }
    }
    
}
